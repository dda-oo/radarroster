const savedLang = localStorage.getItem('language');
if (savedLang === 'de' || savedLang === 'en') {
    window.currentLang = savedLang;
}

window.setLanguage = function(lang) {
    window.currentLang = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    updatePageContent();
    updateLanguageButtons();
}

window.updateLanguageButtons = function() {
    const buttons = document.querySelectorAll('[data-lang-btn]');
    buttons.forEach(btn => {
        if (btn.dataset.langBtn === window.currentLang) {
            btn.classList.add('text-brand-blue', 'font-bold');
            btn.classList.remove('text-gray-400');
        } else {
            btn.classList.remove('text-brand-blue', 'font-bold');
            btn.classList.add('text-gray-400');
        }
    });
}

window.updatePageContent = function() {
    const t = translations[window.currentLang];

    document.querySelectorAll('[data-i18n-html]').forEach(element => {
        const keys = element.dataset.i18nHtml.split('.');
        let value = t;

        for (const key of keys) {
            value = value[key];
            if (!value) break;
        }

        if (value) {
            element.innerHTML = value;
        }
    });

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const keys = element.dataset.i18n.split('.');
        let value = t;
        
        for (const key of keys) {
            value = value[key];
            if (!value) break;
        }
        
        if (value) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = value;
            } else if (element.tagName === 'OPTION') {
                element.textContent = value;
            } else if (element.children.length) {
                const textNode = Array.from(element.childNodes).find(
                    node => node.nodeType === Node.TEXT_NODE && node.textContent.trim().length
                );
                if (textNode) {
                    textNode.textContent = ` ${value} `;
                } else {
                    element.textContent = value;
                }
            } else {
                element.textContent = value;
            }
        }
    });
}

